def คำนวณผลรวม():
    ผลรวมบวก = 0
    ผลรวมลบ = 0
    
    while True:
        ตัวเลข = int(input("กรุณาป้อนตัวเลข : "))
        
        if ตัวเลข > 0:
            ผลรวมบวก += ตัวเลข
        elif ตัวเลข < 0:
            ผลรวมลบ += ตัวเลข
        elif ตัวเลข == 0:
            return ผลรวมบวก, ผลรวมลบ
ผลรวมบวก, ผลรวมลบ = คำนวณผลรวม()

